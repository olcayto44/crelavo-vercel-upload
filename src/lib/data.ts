export function findCreditProduct(productIdOrName: string) {
  const normalized = productIdOrName.trim().toLowerCase();
  return allCreditProducts.find((item) => item.id.toLowerCase() === normalized || item.name.toLowerCase() === normalized);
}

export function findPaymentProduct(productIdOrName: string) {
  const normalized = productIdOrName.trim().toLowerCase();
  return allPaymentProducts.find((item) => item.id.toLowerCase() === normalized || item.name.toLowerCase() === normalized);
}

export function stripePriceEnvForCreditProduct(product: (typeof allCreditProducts)[number], billing: "monthly" | "yearly" | "one_time") {
  if ("stripePriceEnv" in product) return product.stripePriceEnv;
  return billing === "yearly" ? product.yearlyStripePriceEnv : product.monthlyStripePriceEnv;
}

export function stripePriceEnvForPaymentProduct(product: (typeof allPaymentProducts)[number], billing: "monthly" | "yearly" | "one_time") {
  if ("stripePriceEnv" in product) return product.stripePriceEnv;
  if ("monthlyStripePriceEnv" in product && "yearlyStripePriceEnv" in product) return billing === "yearly" ? product.yearlyStripePriceEnv : product.monthlyStripePriceEnv;
  return "";
}

